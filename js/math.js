/**
 * Math 工具函数
 */

/**
 * 第1次 添加add函数
 * @param {*} num1 数字1
 * @param {*} num2 数字2
 * @returns 两数之和
 */
function add(num1, num2) {
  console.log(num1, num2);

  return num1 + num2;
}

/**
 * 第2次 添加减法函数
 * @param {*} num1 数字1
 * @param {*} num2 数字2
 * @returns 两数之差
 */
function minus(num1, num2) {
  console.log(num1, num2);

  return num1 - num2;
}

/**
 * 第3次 添加乘法函数
 * @param {*} num1 数字1
 * @param {*} num2 数字2
 * @returns 两数乘积
 */
function multi(num1, num2) {
  console.log(num1, num2);

  return num1 * num2;
}

/**
 * 第4次 添加除法函数
 * @param {*} num1 数字1
 * @param {*} num2 数字2
 * @returns 两数之商
 */
function division(num1, num2) {
  console.log(num1, num2);

  return num1 / num2;
}

